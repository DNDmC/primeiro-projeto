// Vetores e Matrizes 
// Abaixo vemos um exemplo de Vetor (Lista)
let pokemon = ["Pikachu", "Charmander", "Bulbassaur"]

// A função pop exclui o ultimo elemento de um array/vetor (lista) nesse caso removeria Bulbassaur
pokemon.pop()

// A função shift exclui o primeiro elemento de um array/vetor (lista) nesse caso removeria Pikachu
pokemon.shift()

console.log(pokemon)